public class TrainGoal {
    public void advance() {
        System.out.println("The train is moving foward.");
        return;
    }

    public void goBack() {
        System.out.println("The train is going back.");
        return;
    }

    public void stop() {
        System.out.println("The train stopped.");
        return;
    }

    public void whistle() {
        System.out.println("The train whistled");
        return;
    }   
}
